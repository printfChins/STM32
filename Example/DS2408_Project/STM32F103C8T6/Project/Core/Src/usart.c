void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart, uint16_t Size){
    if(huart == &huart1){
		    HAL_UART_Transmit_DMA(&huart1, rxBuffer, Size);											// 列印出文字
		    HAL_UARTEx_ReceiveToIdle_DMA(&huart1, rxBuffer, sizeof(rxBuffer));	// 啟動下次DMA中斷
			  __HAL_DMA_DISABLE_IT(&hdma_usart1_rx, DMA_IT_HT);										// 關閉過半回調中斷 (沒關閉時，當Size達一半時會進入中斷，導致數據不完整)
		}
}

